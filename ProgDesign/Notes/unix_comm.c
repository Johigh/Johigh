/*
Text editor : UNIX 
- 'vi test.c' 
- ":wq" - save changes to current file and quit
- ":w" - save changes w/o quitting
- ":w filename" - save changes to new file of "filename" w/o quitting
- ":q" - Quit current file, no changes from last write have been made. 
- ":q!" - Ignore change sto current file adn quit. No changes from last write will be saved. 
- "yy" and "p" - copy and paste 
- "dd" - delete
- u = undo  
*/